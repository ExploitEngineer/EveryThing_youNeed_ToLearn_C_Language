#include <stdbool.h>

int main(void)
{
    int a;          // Integer
    unsigned int b; // Unsinged integers only store positive numbers. As result, they have higher positive range.
    char c;         // Character
    short d;        // Short Integer
    long e;         // Long Integer
    float f;        // Floating point integer
    double g;       // Double-precision floating point integer
    bool h;         // Boolean TRUE or FLASE
    return 0;
}
